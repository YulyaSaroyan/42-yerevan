#include "libft.h"

int	ft_strlcpy(char *dest, char *src, size_t size)
{
	int		len;
	size_t	i;

	len = 0;
	while (src[len])
		len++;
	if (size == 0 || !dest)
		return (len);
	i = 0;
	while (i < size - 1 && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (len);
}
